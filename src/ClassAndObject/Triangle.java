package ClassAndObject;

public class Triangle {
    private int side1;
    private int side2;
    private int side3;
    public Triangle() {
        side1 = 3;
        side2 = 4;
        side3 = 5;
    }
    public int getSide1() {
        return side1;
    }
    public int getSide2() {
        return side2;
    }
    public int getSide3() {
        return side3;
    }
}
